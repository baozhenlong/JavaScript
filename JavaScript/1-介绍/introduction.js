//1---什么是JavaScript语言
//JavaScript是一种轻量级的脚本语言
//脚本语言：指的是它不具备开发操作系统的能力，而是只用来编写控制其他大型应用程序(比如浏览器)的"脚本"
//JavaScript也是一种嵌入式语言
//它本身提供的核心语法不算很多，只能用来做一些数学和逻辑运算
//JavaScript本身不提供任何与I/O(输入/输出)相关的API，都要靠宿主环境(host)提供
//所以JavaScript只适合嵌入更大型的应用程序环境，去调用宿主环境提供的底层的API

//2---JavaScript与Java的关系
//JavaScript和Java是两种不一样的语言，但是彼此存在联系
//JavaScript的基本语法和对象体系，是模仿Java而设计的；但是，JavaScript没有采用Java的静态类型
//JavaScript语言的函数是一种独立的数据类型，以及采用基于原型对象(prototype)的继承链
//Java语言需要编译，而JavaScript语言则是运行时由解释器直接执行

//3---JavaScript与ECMAScript的关系
//ECMAScript是JavaScript的规格；ECMAScript只用来标准化JavaScript这种语言的基本语法结构
